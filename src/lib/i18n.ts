const dictionaries: Record<Language, Dict> = { en, pl };

export function t(key: string, params?: Record<string, string | number>): string {
  const lang = get(language);
  let s = dictionaries[lang][key] ?? en[key] ?? key;
  if (params) s = s.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? ''));
  return s;
}

/** React hook: subscribes to the language so the component re-renders on a switch. */
export function useT(): typeof t {
  useStore(language);
  return t;
}
